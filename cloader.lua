function c_map()
    syscall.resolve({ mprotect = 74 })
    local PROT_READ = 0x1
    local PROT_WRITE = 0x2
    local PROT_EXEC = 0x4
    local PROT_COMB = bit32.bor(PROT_READ, PROT_WRITE, PROT_EXEC)

    local MAP_PRIVATE = 0x02
    local MAP_FIXED = 0x10
    local MAP_ANONYMOUS = 0x1000
    local MAP_COMB = bit32.bor(MAP_PRIVATE, MAP_ANONYMOUS, MAP_FIXED)

    -- alloc executable memory
    local map = syscall.mmap(0xfe000000, 0x1000, PROT_COMB, MAP_COMB, -1, 0)
    if map:tonumber() < 0 then
        error("mmap() error: " .. get_error_string())
    end

    printf("mapped executable memory at: 0x%x\n", map:tonumber())

    -- following payload will call sceNotify function and print "test"
